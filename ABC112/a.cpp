#include <bits/stdc++.h>
using namespace std;

int main(void)
{
    int n;
    cin >> n;
    switch (n)
    {
        case 1:
            cout << "Hello World" << endl;
            break;
        case 2:
            int a, b;
            cin >> a >> b;
            cout << a + b << endl;
            break;
        default:
            break;
    }
    return 0;
}